package wkb

import (
	"bytes"
	"reflect"
	"testing"
)

type testCase struct {
	s    string
	want Geom
}

func testRead(t *testing.T, tc testCase) {
	r := bytes.NewBufferString(tc.s)
	if got, err := Read(r); !reflect.DeepEqual(got, tc.want) || err != nil {
		t.Errorf("Read(%q) == %q, %q, want %q, nil", tc.s, got, err, tc.want)
	}
}

func testReads(t *testing.T, tcs []testCase) {
	for _, tc := range tcs {
		testRead(t, tc)
	}
}

func TestReadPoint(t *testing.T) {
	testRead(t, testCase{"\x01\x01\x00\x00\x00\x00\x00\x00\x00\x00\x00\xf0?\x00\x00\x00\x00\x00\x00\x00@", Point{1, 2}})
}

